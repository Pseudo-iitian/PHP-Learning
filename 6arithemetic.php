<?php
  // +, -, *, / , %, ++, --, ** power of exponentition
  $a = 2;
  $b = 4;
  echo "sum is: ",$a+$b,"\n";
  echo "subtraction is: ",$a - $b,"\n";
  echo "division is: ",$a/$b,"\n";
  echo "multiplication is: ",$a*$b,"\n";
  echo "modulo is: ",$a%$b,"\n";
